// Resolve a free-text city name to coords. Tries exact match first, then prefix.
export function resolveCity(input: string): { name: string; lat: number; lng: number } | null {
  const norm = input.trim().toLowerCase();
  if (!norm) return null;
  if (UA_CITIES[norm]) return UA_CITIES[norm];
  // Try without "м." or "г." prefix
  const stripped = norm.replace(/^(м\.|м |г\.|г )\s*/i, '').trim();
  if (UA_CITIES[stripped]) return UA_CITIES[stripped];
  // Prefix match
  for (const key of Object.keys(UA_CITIES)) {
    if (norm.startsWith(key) || key.startsWith(norm)) return UA_CITIES[key];
  }
  return null;
}
